<p> &gt;&gt; Roles: K-Life Nursery, K-Life Preschool, Nursery Teacher, Toddler Teacher, Preschool Teacher, Early Childhood Check-In.</p>
<p> &gt;&gt; Time commitment: Once or twice a month, as needed.</p>
<p></font><br />
  <font size="2"><b>LifeKids Elementary</font></b><br />
</p>
<font size=1>&gt;&gt; Description: Life Kids Elementary is a ministry for children grades Kindergarden-5th grade, which is centered on creatively and effectively communicating God's word to children. We want them to experience a relationship with God that is fun and exciting, to know that they are accepted and loved, and to ensure families that we will care for their children. We need volunteers for everything from teaching the classes and checking in the children, to running A/V during the weekend services.
<p> &gt;&gt; Roles: Weekend Small Group Leader, Weekend Large Group Leader, Weekend Assistant, Weekend Worship Leader, Midweek Teacher, Midweek Assistant Teacher, Midweek Coordinator.</p>
<p> &gt;&gt; Time commitment: Once or twice a month, as needed.</p>
<p></font><br />
  <font size="2"><b>Student Life Ministry</font></b><br />
</p>
<font size=1>&gt;&gt; Description: Life is a ministry focused on students. Everything we do is about taking students to the next level. Our weekly services on Wednesday nights provide an atmosphere for high school and junior high students to bring their friends and connect in a way that is relevant to today's progressive culture. There are several areas to be involved in such as A/V, worship team, set-up/tear down, media (videos, website, graphics, etc.), administration, small groups, security, and cafe.
<p> &gt;&gt; Roles: Junior High Leader, or Senior High Leader.</p>
<p> &gt;&gt; Time commitment: Wednesday nights from 6:00 - 9:00 PM.</p>
