<p><br />
  <font size="2"><b>Parking Lot Attendant</font></b><br />
<font size=1>&gt;&gt; Description: Parking lot Attendants are the first people guests will see when they come to Life Church, so a big smile and an ability to brave all types of weather are a must! As a parking lot attendee you will be responsible for Directing traffic, set-up/tear down of signage and drop zones, and welcoming people each weekend.
<p> &gt;&gt; Time commitment: Once or twice a month (30 minutes prior to service). </p>
<br><font size=2><b>Greeter</font></b><br />
</p>&gt;&gt; Description: Do you love to smile? Greeters are arguably the most important Volunteer here at Life Church. They help make newcomers feel welcome, excepted, and comfortable here. Greeters are responsible for welcoming people as they enter the building/auditorium, handing out bulletins, and helping people find where they need to go (Life Kids, restrooms, etc.).  Great people skills are a must!
<p> &gt;&gt; Time commitment: 4 services every other month (30 minutes prior to service). </p>
<br><font size=2><b>Usher</font></b><br />
</p>&gt;&gt; Description:  Once a Guest has been greeted, someone must be there to guide them to their seats. This is where the usher comes in. They are responsible for making sure everyone gets a seat, both newcomers and regular attendees, alike. They also are responsible for keeping order in each weekend service, and helping receive the tithe and offerings during each of the services. Must have good people skills.
<p> &gt;&gt; Time commitment: 4 services every other month (2 hours per service). </p>
<br><font size=2><b>Guest Services</font></b><br />
</p>&gt;&gt; Description:  Before and after services there are always people with questions about Life Church. Guest Services is there to help answer any of these questions. Whether its helping people get information, sign up for events, the sale of Life Church media, or general directions, Guest services is always there to help. If you like helping people and love to smile this job is for you!
<p> &gt;&gt; Roles: Guest Services, Media Sales.</p>
<p> &gt;&gt; Time commitment: Twice a month (30 minutes prior to service and 30 minutes after service).</p>
