<FONT color="#939496">  What is Married Life Live?
<i>  Married Life Live is a two-hour event where you and your spouse 
	can enjoy a fun night out together while conversing over 
	coffee, dessert, connecting with other couples, enjoying live 
	music, or laughing together. The night is centered around one 
	of the six essentials of a healthy marriage.</i>

  What are the six essentials?
<i>  Each Married Life Live event is centered around one of the six 
	essentials of a healthy marriage.  Here they are:
		Trust God 
		Hurry Home (we will be discussing this one on Sep. 24)
		Cultivate Communication
		Nurture Romance
		Celebrate Differences
		Finish Together
</i>


  Is childcare available?
<i>  No. In order to maximize both your experience and that of other couples, 
	we kindly ask that you personally arrange for childcare.</i>

  Is this a marriage conference?
<i>  No.  This is simply a night of entertainment and encouragement.  Don’t 
	worry guys, you won’t have to share your feelings with a group!</i>

  Will there be food?
<i>  Of course!  What would a Life Church event be without food?  We will 
	serve dessert and coffee...and plenty of it!</i>

  What time should I get there?
<i>  The doors will open at 6:30 PM, and the main event will start at 7:00 PM.  
	We recommend getting there at least 15 minutes before 7:00 so you have 
	time to get a table, grab some food, and get settled in.</i>

  How should I dress?
<i>  No need to get too fancy.  It will be a casual atmosphere, so yes, jeans 
	are fine.</i>
	
	
