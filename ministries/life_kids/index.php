					<h2>LifeKids Early Childhood</h2>
						<p>LifeKids Early Childhood is our environment for preschool aged kids (birth – pre-K). It is the place where parents bring their preschoolers and volunteers show up every Sunday to introduce these little ones to their Heavenly Father. And that’s the <b>MOST IMPORTANT</b> thing we do for our preschoolers. We want to make a <b>FIRST IMPRESSION</b> in such a way that it makes a <b>LASTING IMPRESSION</b> on the heart of a child .
					</p>
					
					<h2>LifeKids Elementary</h2>
						<p>LifeKids Elementary is our environment for ages K – 5th grade where they can discover and develop a passion for God. We accomplish this through awesome worship (with a live band) cool videos; teaching and small groups lead by dedicated adult volunteers. We want kids to see how God's word fits into their lives, to learn how to talk to Him, and to build friendships that will last. Before Kids head off to middle school, we want to make sure they know these 3 things:<br> <br> 		
						1. I need to make the wise choice. <br> 
						2. I can trust God no matter what. <br>
						3. I should treat others the way I want to be treated. <br> 
					</p>
					
					
