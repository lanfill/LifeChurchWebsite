<b>What is KidsFest?</b>
Kids Fest is an annual outreach of Life Church 
to our community. Find out more about Life Church by 
visiting the "Our Story" section of this website.

<b>Where is KidsFest?</b>
KidsFest is located at Life Church:
W164N11325 Squire Dr
Germantown, WI

<b>When does KidsFest2010 Start?</b>
Doors open at 5:30pm and close at 8:30pm

<b>Who is this event for?</b>
KidsFest is geared to Elementary students and their 
families.

<b>How much does it cost?</b>
KidsFest is a FREE event for our community. Admission and 
all games and rides are FREE. There will be concessions 
for purchase.

<b>The event is on Halloween, can my child wear a costume?</b>
YES! Costumes are welcome, we ask that the costumes be 
appropriate for our age group. Parents may wear costume's 
as well (Adults and teenagers with mask's will be asked to 
remove them for security reasons)

<b>What is the Big Show and do I need tickets?</b>
The Big Show is a family type show where you will be able 
to participate in some fun songs, learn a cool lesson and 
also have a chance to win some cool prizes like a DS or PSP!
You do not need a ticket for the Big Shows. You will be able 
to pick which show you would like to attend when you register 
at the event.

<b>Will my child get candy?</b>
TONS!!

<b>Will my child have fun?</b>
CRAZY AMOUNTS!!


