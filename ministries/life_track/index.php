<p>
The Life Track is a catalyst to help you become a fully devoted follower of Jesus Christ. The Track offers the tools necessary to make this happen in your life. From the initial decision to accept Jesus as your Lord to your final destination in life-changing ministry, Pastor Aaron and the Life Church staff will lead you step-by-step from healing to growth and into a fulfilling leadership role.
</p>

<h2>Who is it for?</h2>
<p>If you want to learn more about Life Church, are interested in becoming a member, or want to volunteer, Life Track is the next step you need to take.</p>

<h2>4 Sessions</h2>
<p>Life Track consists of 4 sessions, with the first three taught in consecutive Wednesday nights at 6:30 PM.  The fourth session will take place on a Sunday afternoon.  Here is an overview of each session:</p>

