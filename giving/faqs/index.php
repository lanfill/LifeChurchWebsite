     <div id="page_content">    <h2>Frequently Asked Questions</h2>
    <br>
    <p>Q: Is it safe to give online?<br>
      Yes. In many ways giving online is safer than writing a check because an electronic 
      gift cannot be lost or stolen. The security of the system is continually managed by 
      Fellowship One.<br>
      <br>
      Q: What types of bank accounts can I give from? <br>

      You can give online from your checking account, MasterCard, Visa, Discover, or 
      American Express. <br>
      <br>
      Q: Are there any fees involved with giving online?<br>
      Not to you. You will not pay any fees with an online gift. In addition, online gifts are 
      a more cost efficient way for the church to process donations. <br>
      <br>
      Q: Can I make a one-time contribution? <br>
      Yes. The system allows you the option of either making a one-time contribution or 
      setting up a recurring contribution. For a one-time contribution, you can designate 
      that the contribution should be made immediately or, for both one-time and 
      recurring contributions, you can schedule the contributions to come out of your 
      bank account on the date(s) specified by you. <br>

      <br>
      Q: If I want to set-up a recurring gift, what are my options for frequency of 
      my gift? <br>
      For recurring gifts, you have the option of giving weekly, twice a month, every two 
      weeks or once a month. <br>
      <br>
      Q: Can I change my personal information or the amount or the frequency of 
      my gift once I have set it up? <br>
      Yes. You can change or cancel your contribution at any time before the date of your 
      next contribution. Simply log in to the system using your user name and password 
      and make the necessary changes in the system. <br>
      <br>

      Q: Can I review my donation history online? <br>
      Yes. The site will allow you to view the complete history of your contributions, given 
      either online or otherwise. Once you’ve created an account, you will able to 
      contribute immediately. However, for security purposes, before you can see any 
      past contributions, we must manually connect your online account with your 
      previous information. This is a one-time process when you initially create your 
      account. After that, you can view your history at any time. For more information, 
      see our <a href="/giving/security/" style="color:#93a881">security policy</a>. <br>
      <br>
      Q: Will I still receive regular contribution statements from the church? <br>
      Yes. The church will continue to send year-end contribution statements to your 
      address on file for tax purposes. <br>
      <br>

      Q: When will contributions be taken from my account? <br>
      Contributions will be taken from your specified bank account within 48 business 
      hours of the date you requested. This time frame allows time for the contribution to 
      process through your bank and the church’s bank. If the date of your contribution 
      falls on a weekend or a holiday, the transaction will be initiated on the next banking 
      day. <br>
      <br>
      Q: Can I designate my gift to a particular cause?<br>
      Yes, you can designate your gift to go toward tithes and offerings or our poor and 
      needy fund. We may also add additional causes at specific times.<br>
      <br>
      Q: How will I know that I set up my gift correctly? <br>

      Immediately after submitting your contribution, you will receive an e-mail verifying 
      your contribution.<br>
      <br>
      Q: Does it matter which Internet browser I use? 
      No. Any browser will work.<br>
      <br>
      Q: Will I be able to give to special Life Church campaigns (Year End Fund, 
      Food Drive, etc.) online? <br>
      During times when these campaigns are occurring, you will be able to give a one- 
      time contribution to support the given initiative or campaign.<br>
      <br>

      Q: I have additional questions about online giving that have not been 
      addressed. Who can I talk to?<br>
      For any questions, concerns or comments about the online giving system, please 
      contact us at <a href="mailto:gary@lifechurchwi.com?subject=[Online%20Giving]%20Online%20Giving%20Question" style="color:#93a881">gary@lifechurchwi.com</a>. You will receive a response from 
      our staff as soon as possible. <br>
      <br>
      Q: What types of bank accounts can I give from? <br>
      You can give online from your bank account via an ACH transaction. You can also 
      give using MasterCard, Visa, Discover, or American Express. <br>
      <br>

      Q. Can I use a debit card? <br>
      You can use a debit check card from Visa or MasterCard, but true debit cards are 
      not accepted through the Fellowship One system due to the sensitivity of securely 
      requesting a Personal Identification Number (PIN). ACH bank drafts can be used 
      instead for true debits. </p>
