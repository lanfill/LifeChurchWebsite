     <div id="page_content">    <h2>Security</h2>
    <br>
    <p>At Life Church, we use an online database called Fellowship One. Their system 
      allows us to provide access to your contribution records and allows you to make 
      donations online. They provide all of the security for the system. This system is 
      used by churches throughout the country and has been fully tested by them.<br>
      <br>
      There are two aspects of security related to the system. First, Fellowship One 
      provides data security using the latest security technology. The following statement 
      is from their security documentation: "[the system] utilizes the most current 
      encryption technology to maintain the security of your data. Every parameter 
      passed in the hyperlinks is fully encrypted. Additionally, the Login module handles 
      passwords using what is known in technical terms as salted hash. In laymenâ€™s 
      terms, this means it would take 50 computers (running constantly) continuously 
      presenting password combinations four years to discover the password."<br>
      <br>
      The second aspect of security relates to account creation. When you first create a 
      new account in the system, you will be able to donate immediately, but you will not 
      have access to any previous contribution information. Before that can happen, we 
      must manually link your previous information to the account you created. 
      In order to verify the authenticity of the account request, we will ensure that all of 
      your data matches our current records (e.g., address, phone number, and email 
      address). If there is any question, we will contact you via email before creating the 
      link. Since this is a manual process, we will perform these steps approximately once 
      a week, but if you would like to speed up this process, you can send an email to <a href="mailto:gary@lifechurchwi.com?subject=[Online%20Giving]%20Online%20Giving%20Question" style="color:#93a881">gary@lifechurchwi.com</a>. <br>

      <br>
      When you create your account, you will determine your own login id and password. 
      If you forget your login id or password, you can have the system email it to you by 
      entering the email address that you used when you created your account. If you 
      need help or cannot remember the email address you used, you can contact us at <a href="mailto:garylifechurch@gmail.com?subject=[Online%20Giving]%20Online%20Giving%20Question" style="color:#93a881">gary@lifechurchwi.com</a>. You can also change your password at any time.<br>
      <br>
      Please help us keep your information safe by protecting your login information. 
      Access to sensitive personal and financial information can be gained by letting 
      someone else use your account or by leaving the information for someone else to 
      see. Life Church will never call you to ask for your login information. </p>
