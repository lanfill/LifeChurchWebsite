      <div id="page_content">    <h2>Vision</h2>
    <p>Life Church was planted in September of 2000 by Evangel Assembly of God in Milwaukee. The vision for Life Church was to have a relevant influence in Germantown. That vision became a reality on Sunday, September 17, 2000. Our first service was held in the Germantown High School auditorium with approximately 35 people in attendance. Those 35 people began to pray and reach their community for Christ. </p>

    <h2>Storefront</h2>
    <p> One year later, Life Church leased a 4,000 square foot unit at the Germantown Center (present location) with about 65 people in Sunday morning attendance. </p>

    <h2>Growth</h2>
    <p>In November of 2002, one hundred people welcomed Pastor Aaron and Tammy Cole to become the new Senior Pastors of Life Church. The church began to grow rapidly, and as a result, Life Church expanded its facilities. Life Church also expanded its ministries and added a second Sunday morning service to continue to allow for the growth of its weekend attendance.  By 2005, Life Church added multiple paid staff members, and a third weekend service on Saturday night.</p>

    <h2>Prime Property</h2>
    <p>In 2006, Life Church had an incredible opportunity to buy the property that was being leased (present location) for $1.75 million, which was nearly $2.5 million less than the appraised value.  Within 2 years, Life Church had expanded its sanctuary to nearly double the size of the previous sanctuary, and opened a 5,000 square foot Student Center.</p>
    
    <h2>Today</h2>
    <p>It's hard to believe Life Church has now grown to over 850 people each weekend, even reaching over 1200 on a Sunday.  Over 2100 people have made decisions for Christ, over 300 people have been water baptized, and over $1 million has been given to missions since its inception just 10 years ago.  God is doing amazing things, and Life Church cannot wait to see what the next chapter in the journey will be.</p>
    
    <h2>ONE CHURCH. TWO LOCATIONS</h2>
    <p>In October 2010, Life Church welcomed Jeremy and Laura Chapman to be campus pastors of our West Campus, which will launch in April 2011. We are excited to expand ministry into the western suburbs of Milwaukee.</p>
